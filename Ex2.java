public class Ex2 {
    public static void main(String[] args) {

        int nr1 = 78;
        float nr2 = 3.14f;

        System.out.println("Adunare numere: " + (nr1 + nr2));
        System.out.println("Scadere numere: " + (nr1 - nr2));
        System.out.println("Inmultire numere: " + (nr1 * nr2));
        System.out.println("Impartire numere: " + (nr1 / nr2));
        System.out.println("Modulo numere: " + (nr1 % nr2));
    }
}
